public class ILoveJava {
	public static void main(String[] args) {
		//Declare constants
		final int LOOP_COUNT = 10;
		
		//Declare loop counter
		int counter = 1;
		
		//Loop while counter is less than loop count
		while(counter++ <= LOOP_COUNT) {
			System.out.println("I love java"); //Print text
		}
	}
}
